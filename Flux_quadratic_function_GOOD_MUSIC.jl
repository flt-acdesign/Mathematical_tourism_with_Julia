using Flux, Plots, PlutoUI, Sound  # import libraries for machine learning and plotting

# ╔═╡ 32f6f7b2-78b5-4d5a-be4d-c7faaf6c67be
# Define the ground truth, the underlaying true data
ground_truth(x) = 3 * x^3  

# ╔═╡ 4262e962-b8f2-45d2-a0db-398cc3b0ef35
# Generate the training data using the ground truth function and add noise
function generate_data(n)  
    x_vals = randn(n)  # generate an array of random numbers
    y_vals = ground_truth.(x_vals) + randn(n)  # add some Gaussian noise to the truth
    return x_vals, y_vals  # return the training points as x and y values
end

# ╔═╡ 1df35841-27d7-4f69-9711-b9726c1bf838
begin
	# Generate training data
	x_train, y_train = generate_data(100)  # obtain the data for training (1oo points)
	# convert the data into a row vector for Flux
	Y_train = Float32.(reshape(y_train, 1, :)) # Flux prefers Float32 data (for GPU)
	X_train = Float32.(reshape(x_train, 1, :))
end

# ╔═╡ fbf6d61c-3cf1-4ad2-82af-82cda1d314f8
@bind n_neurons Slider(1:20, default = 10, show_value = true )

# ╔═╡ cc31b1f2-bca2-4e29-87aa-a5ec7201efc9
# Define the neural network as a multilayer perceptron
model = Chain(   # stack layers of "neurons"
    Dense(1, n_neurons, relu),  # a layer with 1 input, n_neurons "neurons" 
	Dense(n_neurons, n_neurons, relu),  # a layer with n_neurons inputs and n_neurons "neurons"
	Dense(n_neurons, 1)  # output layer, taking n_neurons inputs from the previous layer into a single value
)

# ╔═╡ 7174c589-ae66-4eb7-99ad-60b4e01b98da
begin
	# Define neural network training parameters
	loss(x, y) = Flux.mse(model(x), y)  # Use a mean squared error loss
	optimizer = Descent(0.01)  # Choose an optimizer
	num_epochs = 100 	# number of training iterations
	training_loss = zeros(num_epochs) # allocate a vector to store the loss history
end

# ╔═╡ 71cd4548-2b91-4aa1-af26-e806720f97eb
# function giving the difference between the ground truth and the model at a point x
err_Ground_vs_Model(x) = abs(ground_truth(x) - (model(reshape(Float32.([x]), 1, :)))'[1])

# ╔═╡ 119ab01d-c8af-4fe3-bebd-d9b0b5ec166a
begin

# create an independent set of x points to check the model (different from the points used in the training data to check for overfitting and other issues)	
	x_test = -3:.1:3  # a range from .3 to 3 in steps of 0.1
	
# Create plot for the training loss evolution through the "epochs"
	 loss_evolution = plot(training_loss, xlabel="Epoch", ylabel="MSE Loss", label="Training Loss", legend=:topleft)
	
# Create a plot with the ground truth, training data and model (neural network)approximation
		model_fit = plot(x_test, ground_truth.(x_test), label="Ground Truth", xlabel="X", ylabel="Y", legend=:topleft)
	
		scatter!(x_train, y_train, label="Training Data", xlabel="X", ylabel="Y", legend=:topleft)
	
		plot!(x_test, (model(reshape(Float32.(collect(x_test)), 1, :)))', label="Neural Network Approximation", linestyle=:dash)

# Plot the two plots from above in a grid with 1 row and 2 columns
	plot(loss_evolution, model_fit, layout = (1,2))
end

